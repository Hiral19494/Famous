package ca.android.famous.model;

public class PostComment {
    String id;
    String name;
    String body;

    public String getId() {
        return id;
    }

    public void setId(String ixd) {
        id = ixd;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
